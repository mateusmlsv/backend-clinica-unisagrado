    reumatismo ENUM('S', 'N'),
    cardiovascular ENUM('S', 'N'),
    gastrite ENUM('S', 'N'),
    diabetico ENUM('S', 'N'),
    desmaiou ENUM('S', 'N'),
    tratamentos ENUM('S', 'N'),
    medicamentos ENUM('S', 'N'),
    doencas_ou_cirurgias ENUM('S', 'N'),
    vicios ENUM('S', 'N'),
    ansiedade ENUM('S', 'N'),
    tuberculose ENUM('S', 'N'),
    sifilis ENUM('S', 'N'),
    hepatite ENUM('S', 'N'),
    sida_aids ENUM('S', 'N'),
    sarampo ENUM('S', 'N'),
    caxumba ENUM('S', 'N'),
    varicela ENUM('S', 'N'),
    outras_doencas varchar(255),
    fumante ENUM('S', 'N'),
    frequencia varchar(100),
    gestacao text,
    parto ENUM('N', 'F', 'C'),
    parto_problemas ENUM('S', 'N'),
    amamentacao ENUM('N', 'M'),
    amamentacao_idade int,
    anestesia ENUM('S', 'N'),
    graves_contagiosas ENUM('S', 'N'),
    vacina ENUM('S', 'N'),
    labios ENUM('N', 'A'),
    mucosa_jugal ENUM('N', 'A'),
    lingua ENUM('N', 'A'),
    soalho_boca ENUM('N', 'A'),
    garganta ENUM('N', 'A'),
    palato_mole ENUM('N', 'A'),
    mucosa_alveolar ENUM('N', 'A'),
    gengivas ENUM('N', 'A'),
    glandulas_salivares ENUM('N', 'A'),
    linfonodos ENUM('N', 'A'),
    atm ENUM('N', 'A'),
    musculos_mastigadores ENUM('N', 'A'),
    palato_duro ENUM('N', 'A'),
    oclusao ENUM('N', 'A'),
    alteracoes text,
    pressao_maxima varchar(10),
    pressao_minima varchar(10),
    diagnostico_presuntivo text,
    exames_complementares text,
    diagnostico_definitivo text,
    tratamento_proservacao text,
    plano_tratamento text,
    patient_id int NOT NULL,
    CONSTRAINT FK_PatientFichaAvalicao FOREIGN KEY (patient_id)
    REFERENCES patients(id)
);