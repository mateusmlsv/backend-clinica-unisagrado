CREATE DATABASE clinica_unisagrado;

CREATE TABLE clinica_unisagrado.patients (
    id int NOT NULL PRIMARY KEY AUTO_INCREMENT,
    name varchar(255),
    dt_birthday DATETIME,
    gender ENUM('F', 'M'),
    weight DOUBLE(40,2),
    height DOUBLE(40,2),
    race ENUM('BRANCA', 'NEGRA', 'PARDA'),
    schooling varchar(255),
    profession varchar(255),
    rg varchar(255),
    CPF varchar(11) UNIQUE,
    marital_status varchar(255),
    nationality varchar(255),
    birth_state varchar(255),
    father_name varchar(255),
    father_nationality varchar(255),
    mother_name varchar(255),
    mother_nationality varchar(255),
    telephone varchar(255),
    cellphone varchar(255),
    address varchar(255),
    address_number varchar(255),
    complement varchar(255),
    district varchar(255),
    CEP varchar(255),
    city varchar(255),
    state varchar(255),
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

